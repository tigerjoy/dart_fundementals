void main() {
  // var denotes variables whose value can be
  // re-assigned sometime in the future (can change over time)
  var myName = "Ranajoy";
  var myNumber = 2134566;

  myName = "Joy";
  myNumber = 1234;

  // Variables created using final cannot change overtime
  // The first assignment is the final value of the variable
  final myFinalName = "Stephen";
  // Not Allowed
  // myFinalName = "Ranajoy";
}
